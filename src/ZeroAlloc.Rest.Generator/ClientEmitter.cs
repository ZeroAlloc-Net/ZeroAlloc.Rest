using System.Collections.Generic;
using System.Text;
using Microsoft.CodeAnalysis;
using ZeroAlloc.Rest.Generator.Models;

namespace ZeroAlloc.Rest.Generator;

internal static class ClientEmitter
{
    private static readonly DiagnosticDescriptor s_conflictingBodyDescriptor = new DiagnosticDescriptor(
        id: "ZRA001",
        title: "Conflicting body attributes",
        messageFormat: "Method '{0}' has both [Body] and [FormBody] parameters; only one is allowed",
        category: "ZeroAlloc.Rest.Generator",
        defaultSeverity: DiagnosticSeverity.Error,
        isEnabledByDefault: true);

    internal static void Emit(SourceProductionContext ctx, ClientModel model)
    {
        // Collect unique method-level serializer types (ordered, deduped)
        var overrideSerializers = model.GetOverrideSerializerTypes();

        // Build a collision-free FQN → field name map
        var serializerFieldMap = new Dictionary<string, string>();
        var usedFieldNames = new List<string>();
        foreach (var st in overrideSerializers)
        {
            var fieldName = GetSerializerFieldName(st, usedFieldNames);
            usedFieldNames.Add(fieldName);
            serializerFieldMap[st] = fieldName;
        }

        var hasQueryParams = false;
        foreach (var m in model.Methods)
            foreach (var p in m.Parameters)
                if (p.Kind == ParameterKind.Query) { hasQueryParams = true; break; }

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Net.Http;");
        sb.AppendLine("using System.Net.Http.Headers;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        if (hasQueryParams)
            sb.AppendLine("using ZeroAlloc.Collections;");
        sb.AppendLine("using ZeroAlloc.Rest;");
        sb.AppendLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            sb.AppendLine($"namespace {model.Namespace};");
            sb.AppendLine();
        }

        sb.AppendLine($"public sealed partial class {model.ClassName} : {model.InterfaceName}");
        sb.AppendLine("{");
        sb.AppendLine("    private static readonly global::System.Diagnostics.ActivitySource _activitySource = new(\"ZeroAlloc.Rest\");");
        sb.AppendLine("    private static readonly global::System.Diagnostics.Metrics.Meter _meter = new(\"ZeroAlloc.Rest\");");
        sb.AppendLine("    private static readonly global::System.Diagnostics.Metrics.Counter<long> _requestsTotal = _meter.CreateCounter<long>(\"rest.requests_total\");");
        sb.AppendLine("    private static readonly global::System.Diagnostics.Metrics.Histogram<double> _requestDurationMs = _meter.CreateHistogram<double>(\"rest.request_duration_ms\");");
        sb.AppendLine("    private readonly System.Net.Http.HttpClient _httpClient;");
        sb.AppendLine("    private readonly ZeroAlloc.Rest.IRestSerializer _serializer;");
        foreach (var st in overrideSerializers)
            sb.AppendLine($"    private readonly {st} {serializerFieldMap[st]};");
        sb.AppendLine();

        // Build constructor parameter list
        var ctorParams = new System.Collections.Generic.List<string>
        {
            "System.Net.Http.HttpClient httpClient",
            "ZeroAlloc.Rest.IRestSerializer serializer"
        };
        foreach (var st in overrideSerializers)
        {
            var fieldName = serializerFieldMap[st];
            ctorParams.Add($"{st} {fieldName.Substring(1)}"); // strip leading '_' for param name
        }

        sb.AppendLine($"    public {model.ClassName}({string.Join(", ", ctorParams)})");
        sb.AppendLine("    {");
        sb.AppendLine("        _httpClient = httpClient;");
        sb.AppendLine("        _serializer = serializer;");
        foreach (var st in overrideSerializers)
        {
            var fieldName = serializerFieldMap[st];
            sb.AppendLine($"        {fieldName} = {fieldName.Substring(1)};");
        }
        sb.AppendLine("    }");
        sb.AppendLine();

        foreach (var method in model.Methods)
            EmitMethod(ctx, sb, model.InterfaceName, method, serializerFieldMap);

        if (hasQueryParams)
        {
            sb.AppendLine("    private static void AppendToUrl(ZeroAlloc.Collections.HeapPooledList<char> list, System.ReadOnlySpan<char> value)");
            sb.AppendLine("    {");
            sb.AppendLine("        for (var i = 0; i < value.Length; i++) list.Add(value[i]);");
            sb.AppendLine("    }");
            sb.AppendLine("    private static void AppendToUrl(ZeroAlloc.Collections.HeapPooledList<char> list, char value)");
            sb.AppendLine("        => list.Add(value);");
        }

        sb.AppendLine("}");

        ctx.AddSource($"{model.InterfaceName}.g.cs", sb.ToString());
    }

    private static void EmitMethod(SourceProductionContext ctx, StringBuilder sb, string interfaceName, MethodModel method, IReadOnlyDictionary<string, string> serializerFieldMap)
    {
        var ctParam = FindCancellationToken(method.Parameters);
        var ctArg = ctParam != null ? ctParam.Name : "default";
        var pathParams = FilterParameters(method.Parameters, ParameterKind.Path);
        var queryParams = FilterParameters(method.Parameters, ParameterKind.Query);
        var bodyParam = FirstOrDefault(method.Parameters, ParameterKind.Body);
        var formBodyParam = FirstOrDefault(method.Parameters, ParameterKind.FormBody);
        var headerParams = FilterParameters(method.Parameters, ParameterKind.Header);

        if (bodyParam != null && formBodyParam != null)
        {
            ctx.ReportDiagnostic(Diagnostic.Create(s_conflictingBodyDescriptor, Location.None, method.Name));
            // Still emit valid (if incomplete) code so compilation continues
            formBodyParam = null; // suppress the FormBody branch
        }

        // If the method has its own [Serializer], use that injected field; otherwise fall back to _serializer.
        var serializerExpr = method.SerializerTypeName != null && serializerFieldMap.TryGetValue(method.SerializerTypeName, out var fieldName)
            ? fieldName
            : "_serializer";

        // IL3051/IL2046: Previously we emitted [RequiresDynamicCode] / [RequiresUnreferencedCode]
        // here, but the user-authored interface doesn't carry those annotations — ILC rejects
        // the mismatch and forces consumers to propagate the Requires attributes to every
        // caller. Switch to [UnconditionalSuppressMessage] which flows no obligation to the
        // caller: the serializer is provided by the consumer, so its trim/AOT-unsafety is
        // their concern, not the client wrapper's.
        sb.AppendLine("    [System.Diagnostics.CodeAnalysis.UnconditionalSuppressMessage(\"Trimming\", \"IL2026\", Justification = \"IRestSerializer is provided by the consumer; its trim-unsafety is their concern. The generated client only forwards the call.\")]");
        sb.AppendLine("    [System.Diagnostics.CodeAnalysis.UnconditionalSuppressMessage(\"AOT\", \"IL3050\", Justification = \"IRestSerializer is provided by the consumer; its AOT-unsafety is their concern. The generated client only forwards the call.\")]");
        sb.AppendLine($"    public async {method.ReturnTypeName} {method.Name}({BuildParamList(method.Parameters)})");
        sb.AppendLine("    {");

        var spanName = $"{interfaceName}.{method.Name}";
        sb.AppendLine($"        using var __activity = _activitySource.StartActivity(\"{spanName}\");");
        sb.AppendLine($"        const string __RestMethodTag = \"{spanName}\";");
        sb.AppendLine($"        const string __httpMethod = \"{method.HttpMethod.ToUpper()}\";");
        sb.AppendLine($"        __activity?.SetTag(\"http.method\", __httpMethod);");
        sb.AppendLine($"        __activity?.SetTag(\"rest.method\", __RestMethodTag);");
        sb.AppendLine($"        var __sw = global::System.Diagnostics.Stopwatch.GetTimestamp();");

        EmitUrlBuilding(sb, method.Route, pathParams, queryParams);
        EmitRequestCreation(sb, method, headerParams, bodyParam, formBodyParam, ctArg, serializerExpr);
        EmitSendAndResponse(sb, method, ctArg, serializerExpr, interfaceName);

        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static void EmitUrlBuilding(StringBuilder sb, string route,
        List<ParameterModel> pathParams, List<ParameterModel> queryParams)
    {
        if (pathParams.Count == 0 && queryParams.Count == 0)
        {
            sb.AppendLine($"        var url = \"{route}\";");
            return;
        }

        // Build the route with path interpolation
        var routeExpr = route;
        foreach (var p in pathParams)
            routeExpr = routeExpr.Replace("{" + p.Name + "}", $"{{Uri.EscapeDataString({p.Name}.ToString())}}");

        if (queryParams.Count == 0)
        {
            sb.AppendLine($"        var url = $\"{routeExpr}\";");
        }
        else
        {
            sb.AppendLine($"        var urlBase = $\"{routeExpr}\";");
            sb.AppendLine("        using var urlBuilder = new ZeroAlloc.Collections.HeapPooledList<char>(urlBase.Length + 64);");
            sb.AppendLine("        AppendToUrl(urlBuilder, urlBase.AsSpan());");
            sb.AppendLine("        var hasQuery = false;");
            foreach (var q in queryParams)
            {
                if (q.IsCollection)
                {
                    sb.AppendLine($"        if ({q.Name} != null)");
                    sb.AppendLine("        {");
                    sb.AppendLine($"            foreach (var __item in {q.Name})");
                    sb.AppendLine("            {");
                    sb.AppendLine($"                if (__item == null) continue;");
                    sb.AppendLine($"                AppendToUrl(urlBuilder, hasQuery ? '&' : '?');");
                    sb.AppendLine($"                AppendToUrl(urlBuilder, \"{q.QueryName}=\".AsSpan());");
                    sb.AppendLine($"                AppendToUrl(urlBuilder, System.Uri.EscapeDataString(__item.ToString()!).AsSpan());");
                    sb.AppendLine("                hasQuery = true;");
                    sb.AppendLine("            }");
                    sb.AppendLine("        }");
                }
                else if (q.IsNullable)
                {
                    sb.AppendLine($"        if ({q.Name} != null)");
                    sb.AppendLine("        {");
                    sb.AppendLine($"            AppendToUrl(urlBuilder, hasQuery ? '&' : '?');");
                    sb.AppendLine($"            AppendToUrl(urlBuilder, \"{q.QueryName}=\".AsSpan());");
                    sb.AppendLine($"            AppendToUrl(urlBuilder, System.Uri.EscapeDataString({q.Name}!.ToString()!).AsSpan());");
                    sb.AppendLine("            hasQuery = true;");
                    sb.AppendLine("        }");
                }
                else
                {
                    sb.AppendLine($"        AppendToUrl(urlBuilder, hasQuery ? '&' : '?');");
                    sb.AppendLine($"        AppendToUrl(urlBuilder, \"{q.QueryName}=\".AsSpan());");
                    sb.AppendLine($"        AppendToUrl(urlBuilder, System.Uri.EscapeDataString({q.Name}.ToString()!).AsSpan());");
                    sb.AppendLine("        hasQuery = true;");
                }
            }
            sb.AppendLine("        var url = new string(urlBuilder.AsReadOnlySpan());");
        }
    }

    private static void EmitRequestCreation(StringBuilder sb, MethodModel method,
        List<ParameterModel> headerParams, ParameterModel? bodyParam, ParameterModel? formBodyParam,
        string ctArg, string serializerExpr)
    {
        sb.AppendLine($"        using var request = new System.Net.Http.HttpRequestMessage(");
        sb.AppendLine($"            System.Net.Http.HttpMethod.{Capitalize(method.HttpMethod)},");
        sb.AppendLine($"            url);");
        sb.AppendLine($"        request.Headers.Accept.Add(new System.Net.Http.Headers.MediaTypeWithQualityHeaderValue({serializerExpr}.ContentType));");

        // Static headers declared with [Header("Name", Value = "...")] are added additively.
        // For Accept, this means both the serializer's content type and the static value will
        // appear in the header. This is intentional — use ConfigureHttpClient to override
        // the serializer's Accept if exclusive control is needed.
        foreach (var (name, value) in method.StaticHeaders)
            sb.AppendLine($"        request.Headers.TryAddWithoutValidation(\"{name}\", \"{value}\");");

        foreach (var h in headerParams)
            sb.AppendLine($"        request.Headers.TryAddWithoutValidation(\"{h.HeaderName}\", {h.Name}?.ToString());");

        if (bodyParam != null)
        {
            sb.AppendLine("        var bodyStream = new System.IO.MemoryStream();");
            sb.AppendLine("        try");
            sb.AppendLine("        {");
            sb.AppendLine($"            await {serializerExpr}.SerializeAsync(bodyStream, {bodyParam.Name}, {ctArg}).ConfigureAwait(false);");
            sb.AppendLine("        }");
            sb.AppendLine("        catch");
            sb.AppendLine("        {");
            sb.AppendLine("            bodyStream.Dispose();");
            sb.AppendLine("            throw;");
            sb.AppendLine("        }");
            sb.AppendLine("        bodyStream.Position = 0;");
            sb.AppendLine("        request.Content = new System.Net.Http.StreamContent(bodyStream);");
            sb.AppendLine($"        request.Content.Headers.ContentType = new System.Net.Http.Headers.MediaTypeHeaderValue({serializerExpr}.ContentType);");
        }

        if (formBodyParam != null)
        {
            sb.AppendLine($"        request.Content = new System.Net.Http.FormUrlEncodedContent({formBodyParam.Name});");
        }
    }

    private static void EmitSendAndResponse(StringBuilder sb, MethodModel method, string ctArg, string serializerExpr, string interfaceName)
    {
        sb.AppendLine("        try");
        sb.AppendLine("        {");
        sb.AppendLine($"            using var response = await _httpClient.SendAsync(request, {ctArg}).ConfigureAwait(false);");
        sb.AppendLine("            __activity?.SetTag(\"http.status_code\", (int)response.StatusCode);");
        sb.AppendLine("            __activity?.SetTag(\"server.address\", request.RequestUri?.Host ?? string.Empty);");
        EmitResponseHandling(sb, method, ctArg, serializerExpr, indent: "            ");
        sb.AppendLine("        }");
        sb.AppendLine("#pragma warning disable EPC12");
        sb.AppendLine("        catch (global::System.Exception __ex)");
        sb.AppendLine("        {");
        sb.AppendLine("            __activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Error, __ex.Message);");
        sb.AppendLine("            throw;");
        sb.AppendLine("        }");
        sb.AppendLine("#pragma warning restore EPC12");
    }

    private static void EmitResponseHandling(StringBuilder sb, MethodModel method, string ctArg, string serializerExpr, string indent = "        ")
    {
        var i1 = indent + "    ";
        if (method.ReturnsVoid)
        {
            sb.AppendLine($"{indent}response.EnsureSuccessStatusCode();");
        }
        else if (method.ReturnsResult)
        {
            sb.AppendLine($"{indent}if (response.IsSuccessStatusCode)");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{i1}var responseStream = await response.Content.ReadAsStreamAsync({ctArg}).ConfigureAwait(false);");
            sb.AppendLine($"{i1}var content = await {serializerExpr}.DeserializeAsync<{method.InnerTypeName}>(responseStream, {ctArg}).ConfigureAwait(false);");
            sb.AppendLine($"{i1}return ZeroAlloc.Results.Result<{method.InnerTypeName}, ZeroAlloc.Rest.HttpError>.Success(content!);");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine($"{indent}else");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{i1}var headers = new System.Collections.Generic.Dictionary<string, System.Collections.Generic.IReadOnlyList<string>>();");
            sb.AppendLine($"{i1}foreach (var kvp in response.Headers)");
            sb.AppendLine($"{i1}    headers[kvp.Key] = new System.Collections.Generic.List<string>(kvp.Value).AsReadOnly();");
            sb.AppendLine($"{i1}return ZeroAlloc.Results.Result<{method.InnerTypeName}, ZeroAlloc.Rest.HttpError>.Failure(");
            sb.AppendLine($"{i1}    new ZeroAlloc.Rest.HttpError(response.StatusCode, headers));");
            sb.AppendLine($"{indent}}}");
        }
        else
        {
            sb.AppendLine($"{indent}response.EnsureSuccessStatusCode();");
            sb.AppendLine($"{indent}var responseStream = await response.Content.ReadAsStreamAsync().ConfigureAwait(false);");
            sb.AppendLine($"{indent}return (await {serializerExpr}.DeserializeAsync<{method.InnerTypeName}>(responseStream, {ctArg}).ConfigureAwait(false))!;");
        }
    }

    private static string BuildParamList(IReadOnlyList<ParameterModel> parameters)
    {
        var sb = new StringBuilder();
        for (var i = 0; i < parameters.Count; i++)
        {
            if (i > 0) sb.Append(", ");
            sb.Append(parameters[i].TypeName).Append(' ').Append(parameters[i].Name);
        }
        return sb.ToString();
    }

    private static string Capitalize(string s)
    {
        if (s.Length == 0) return s;
        return char.ToUpper(s[0]) + s.Substring(1).ToLower();
    }

    /// <summary>
    /// Derives a collision-free private field name for an override serializer from its fully-qualified
    /// type name, deduplicating against already-assigned field names.
    /// "MyApp.OverrideSerializer" → "_overrideSerializer"; collisions get a numeric suffix (_overrideSerializer2, etc.)
    /// </summary>
    private static string GetSerializerFieldName(string fullTypeName, List<string> existingFieldNames)
    {
        var simpleName = fullTypeName.Contains('.')
            ? fullTypeName.Substring(fullTypeName.LastIndexOf('.') + 1)
            : fullTypeName;
        if (simpleName.Length == 0) simpleName = "Serializer"; // defensive fallback
        var candidate = "_" + char.ToLower(simpleName[0]) + simpleName.Substring(1);
        if (!existingFieldNames.Contains(candidate)) return candidate;
        // Collision — append index
        var i = 2;
        while (existingFieldNames.Contains(candidate + i)) i++;
        return candidate + i;
    }

    private static ParameterModel? FindCancellationToken(IReadOnlyList<ParameterModel> parameters)
    {
        for (var i = 0; i < parameters.Count; i++)
            if (parameters[i].Kind == ParameterKind.CancellationToken) return parameters[i];
        return null;
    }

    private static List<ParameterModel> FilterParameters(IReadOnlyList<ParameterModel> parameters, ParameterKind kind)
    {
        var result = new List<ParameterModel>();
        for (var i = 0; i < parameters.Count; i++)
            if (parameters[i].Kind == kind) result.Add(parameters[i]);
        return result;
    }

    private static ParameterModel? FirstOrDefault(IReadOnlyList<ParameterModel> parameters, ParameterKind kind)
    {
        for (var i = 0; i < parameters.Count; i++)
            if (parameters[i].Kind == kind) return parameters[i];
        return null;
    }
}
